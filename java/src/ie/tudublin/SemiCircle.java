public class SemiCircle
{
    private float radius;
    private UI ui;
    private int x;
    private int y;
    private int h;
    private int start;
    private int stop;

    public SemiCircle(UI ui, float radius, int x, int y, int h)
    {
        this.ui = ui;
        this.radius = radius;
        this.x = x;
        this.y = y;
        this.h = h;
        this.start = start;
        this.stop = stop;
    }




}